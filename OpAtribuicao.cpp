#include <iostream>
using namespace std;

int main() {

    int x;
    cout << "Digite um valor: ";
    cin >> x;

    x += 10; //peguei como exemplo o numero 10
    cout << x << endl;

    x += 10;
    cout << x << endl;
    
    x -= 10;
    cout << x << endl;
    
    x *= 10;
    cout << x << endl;

    x /= 10;
    cout << x << endl;
    
    x %= 10;
    cout << x << endl;
    
    

    return 0;
}